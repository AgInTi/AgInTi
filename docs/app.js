const languageSelect = document.querySelector("#language-select");
const translatableNodes = [...document.querySelectorAll("[data-i18n]")];

function normalizeLanguage(language) {
  const lower = language.toLowerCase();
  if (lower.startsWith("zh-hant") || lower.includes("tw") || lower.includes("hk")) return "zh-Hant";
  if (lower.startsWith("zh")) return "zh-Hans";
  return supportedLanguages.find((supported) => lower.startsWith(supported.toLowerCase())) || "en";
}

function applyLanguage(language) {
  const normalized = supportedLanguages.includes(language) ? language : normalizeLanguage(language);
  const dictionary = translations[normalized] || translations.en;
  document.documentElement.lang = normalized;
  document.documentElement.dir = normalized === "ar" ? "rtl" : "ltr";
  document.body.dataset.lang = normalized;
  languageSelect.value = normalized;

  for (const node of translatableNodes) {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  }

  localStorage.setItem("aginti-language", normalized);
}

const savedLanguage = localStorage.getItem("aginti-language");
applyLanguage(savedLanguage || normalizeLanguage(navigator.language || "en"));

languageSelect.addEventListener("change", (event) => {
  applyLanguage(event.target.value);
});
